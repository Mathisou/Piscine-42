#include "ft.h"
int	sum(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int	div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int	sub(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int	mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}

int	mul(int nb1, int nb2)
{
	return (nb1 * nb2);
}
