#include "ft.h"

int	*ft_init_grid(void)
{
	int	x;
	int	*grid;

	x = 0;
	grid = malloc(sizeof(*grid) * 16);
	while (x < 16)
	{
		grid[x] = 0;
		x++;
	}
	return (grid);
}
